    private void tbDSNhanVienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbDSNhanVienMouseClicked
        // TODO add your handling code here:
        int index = tbDSNhanVien.getSelectedRow();
        String id = tbDSNhanVien.getValueAt(index, 0).toString();
        
        btnSua.setEnabled(true);
        btnXoa.setEnabled(true);
        btnThem.setEnabled(false);
        
        try {
            ResultSet rs = query.GetNhanVien_ID(id);
            rs.next();
            
            String name = rs.getString("name");
            String phone = rs.getString("Phone");
            String luong = rs.getString("PayGrade");
            String tenLD = rs.getString("ManagerName");
            String chucvu = rs.getString("JobTitles");
            
            ClearContents();
            ShowComboBox();
            cbChucVu.setSelectedItem(chucvu);
            txtID.setText(id);
            txtTen.setText(name);
            txtSDT.setText(phone);
            txtLuong.setText(luong);
            txtTenLD.setText(tenLD);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_tbDSNhanVienMouseClicked

    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        // TODO add your handling code here:
        if (txtID.getText().equals("") || txtTen.getText().equals("") || txtLuong.getText().equals("") || txtSDT.getText().equals("") || txtTenLD.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Xin nhập đủ thông tin", "Lỗi nhập", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        String id = txtID.getText();
        String name = txtTen.getText();
        String supName = txtTenLD.getText();
        String phone = txtSDT.getText();
        String pay = txtLuong.getText();
        String jobName = cbChucVu.getSelectedItem().toString();
        String[] arrOfString = name.split(" ", 2);
        String fname = arrOfString[1];
        String lname = arrOfString[0];
        
        try {
            if (arrOfString.length < 2) {
                JOptionPane.showMessageDialog(this, "Tên nhập chưa hợp lệ", "Lỗi nhập", JOptionPane.ERROR_MESSAGE);
            }else if (CheckExistedEmployee(id)) {
                if (isNumeric(pay) && isNumeric(phone)) {
                    query.AddNhanVien(id, fname, lname, phone, jobName, pay, supName);
                    JOptionPane.showMessageDialog(this, "Thêm thành công nhân viên " + name);
                    btnThem.setEnabled(false);
                    btnSua.setEnabled(true);
                    btnXoa.setEnabled(true);                    
                } else {
                    JOptionPane.showMessageDialog(this, "Số điện thoại và tiền lương phải là kiểu số", "Lỗi nhập", JOptionPane.ERROR_MESSAGE);
                }
//                JOptionPane.showMessageDialog(this, "Duoc them", "Lỗi nhập", JOptionPane.ERROR_MESSAGE);

            } else {
                JOptionPane.showMessageDialog(this, "Mã nhân viên đã tồn tại.\nXin nhập mã mới", "Lỗi nhập", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
        ShowJobTitles();
        ShowListOfEmployee_JobTitles(cbChucVu.getSelectedItem().toString());
    }//GEN-LAST:event_btnThemActionPerformed

