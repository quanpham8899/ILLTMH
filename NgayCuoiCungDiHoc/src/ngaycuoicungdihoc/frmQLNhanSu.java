    private void ClearEmplyeeList(){
        DefaultTableModel model = (DefaultTableModel)tbDSNhanVien.getModel();
        model.setRowCount(0);
    }
    
    private void ShowListOfEmployee_JobTitles(String title){
        ClearEmplyeeList();
        try {
            DefaultTableModel model = (DefaultTableModel)tbDSNhanVien.getModel();
            ResultSet rs = query.GetDSNV_ChucVu(title);
            while(rs.next()) {
                String id = rs.getString("EmployeeID");
                String hoTen = rs.getString("name");
                String phone = rs.getString("Phone");
                String luong = rs.getString("PayGrade");
                double l = Double.parseDouble(luong);
                DecimalFormat formatter = new DecimalFormat("#,### VNĐ");
                luong = formatter.format(l);
                
                model.addRow(new Object[]{id, hoTen, phone, luong});
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    public boolean isNumeric(String str) { 
        try {  
            Double.parseDouble(str);  
            return true;
        } catch(NumberFormatException e){  
            return false;  
        }  
    }
    
    private void ShowComboBox(){
        cbChucVu.removeAllItems();
        try {
//            cbChucVu.addItem("");
            ResultSet rs = query.GetDSChucVu();
            while (rs.next()) {
                cbChucVu.addItem(rs.getString("JobTitles"));
            }
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    private void ShowJobTitles(){
        dsChucVu.removeAllElements();
        try {
            ResultSet rs = query.GetDSChucVu();
            while (rs.next()) {
                dsChucVu.addElement(rs.getString("JobTitles"));
            }            
            lvDSChucVu.setModel(dsChucVu);
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
    
    private boolean CheckExistedEmployee(String id) throws SQLException {
        ResultSet rs = query.getMaNhanVien(id);
        if (rs.next()) {
            return false;
        } 
        return true;
    }
    
    private void CheckEmployeeID(String id) throws SQLException {
        ResultSet rs = query.getNhanVienFromMa(id);
        if (rs.next()){
//            System.out.println("Result : " + rs.getString("Phone"));
            txtTen.setText(rs.getString("name"));
            txtLuong.setText(rs.getString("PayGrade"));
            txtSDT.setText(rs.getString("Phone"));
            txtTenLD.setText(rs.getString("ManagerName"));
            cbChucVu.setSelectedItem(rs.getString("JobTitles"));
        }else {
            txtTen.setText("");
            txtLuong.setText("");
            txtSDT.setText("");
            txtTenLD.setText("");
        }
    }

